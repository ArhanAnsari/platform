// For categories (it could be lessons or topics that you want to display but this would be good for the search bar)

const { PrismaClient } = require("@prisma/client");

const database = new PrismaClient();

async function main() {
  try {
    await database.category.createMany({
      data: [
        { name: "Tutorials" },
        { name: "Guides" },
        { name: "Live Sessions"}
      ]
    });

    console.log("Success");
  } catch (error) {
    console.log("Error seeding the database categories", error);
  } finally {
    await database.$disconnect();
  }
}

main();